"""
Boolean And Operator
"""

like_coffee = True
like_tea = False
favorite_food = 'Pizza'
favorite_number = 32
print(type(like_coffee))
print(type(like_tea))
print(type(favorite_number))
print(type(favorite_food))

# Comparison Operator
print(1 == 2)
print(1 != 2)
print(1 > 2)
print(1 < 2)
print(1 >= 1)
print(1 <= 2)

#Logical Operators
print(1 > 3 and 5 < 7)
print(1 > 3 or 5 < 7)

